package com.syntax.class07;

public class IncrementAndDecrenent {
	public static void main(String[] args) {
		
		int num=90;
		
		num=num+1;
		System.out.println(num); //result=91
		
		//another way to increment variable by 1 -> ++
		
		num++; //+1
		
		System.out.println(num); //result=92
		
		//to decrement a value of a variable by 1 -> --
		
		num--;
		
		System.out.println(num); //result=91
		
		int i=10;
		i--;
		i--;
		System.out.println(i); //result=8
		
	}
	

}
