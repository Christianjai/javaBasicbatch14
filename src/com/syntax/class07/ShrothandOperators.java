package com.syntax.class07;

public class ShrothandOperators {
	public static void main(String[] args) {
		
		int i=10;
		
		i=i+9;
		System.out.println(i); //result 19
		
		i=i-5;
		System.out.println(i); //result 14
		
		i=i*2;
		System.out.println(i); //result 28
		
		int a=10;
		
		a+=9;
		System.out.println(a); //result 19
		
		a-=5;
		System.out.println(a); //result 14
		
		a*=2;
		System.out.println(a); //result 28
		
		a-=10;
		System.out.println(a); //result 18
		
		a%=4;
		System.out.println("value after mod opeteration = "+a); //result 2
		
		a*=10;
		System.out.println(a); //result 20
		
		a+=1;
		System.out.println(a); //result 21
	}

}
