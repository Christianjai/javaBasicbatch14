package com.syntax.class02;

public class RecapArithmaticOperators {

	public static void main(String[] args) {
		
		/*
		 * Addition/summa -> +
		 * Subtraction/vÃ¤hennys -> -
		 * Multiplication/kertolasku -> *
		 * Division/jako -> /
		 * Modulus -> %
		 */

		int number1=9;
		int number2=2;
		
		System.out.println("Answer = "+number1/number2); //dividing number 1 by number2
		System.out.println("Remainder = "+number1%number2);
		
		double number3=9.0;
		double number4=2.0;
		
		System.out.println(number3/number4); //dividing number 1 by number2
		System.out.println(number3%number4);
	}

}
