package com.syntax.class02;

public class VariableCreation {

	public static void main(String[] args) {
		
		//first way to create a variable
		byte b=10; //create a variable + assign value
		
		//second way to create a variable
		int i; //create a variable
		i=100;	//assign value
		
		//we need to create a variable only one
		//then we can use it as many times we want/need during the program
		
		//third way to crate a variable
		int num1, num2, num3; //we create a variable or declare a variable
		
		num1=100; //assign value or initialize variable
		num2=200; //assign the value
		num3=300;
		
		//int num1=100; ERROR duplicate variable
		
		//can I change a value of a variable?
		num2=2000; //reassign
		
		System.out.println(num2);
		
		//one variable can hold only one value at a time (but can be changed)
		
		
		
		
		
		
		

	}

}
